class Person {
    private String name;
    private String surname;
    private String phone;
    private String age;
    private String gender;
    private String type;

    public Person(String name, String surname, String phone, String age, String gender, String type) {
        this.name = name;
        this.surname = surname;
        this.phone = phone;
        this.age = age;
        this.gender = gender;
        this.type = type;
    }
}
